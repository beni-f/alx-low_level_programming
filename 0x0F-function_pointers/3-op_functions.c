#include <stdio.h>
/**
 * op_add - returns sum of two integers
 *
 * @a: integer input
 * @b: integer input
 * Return: returns 0
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - returns the difference of two integers
 *
 * @a: integer input
 * @b: integer input
 * Return: returns 0
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - returns the product of two integers
 * @a: integer input
 * @b: integer input
 * Return: returns 0
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - returns the quotient of two integers
 * @a: integer input
 * @b: integer input
 * Return: returns 0
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns the remainder of two integers
 * @a: integer input
 * @b: integer input
 * Return: returns 0
*/
int op_mod(int a, int b)
{
	return (a % b);
}
